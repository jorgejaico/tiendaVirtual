package Tiendavirtual.modelo;

/**
 *
 * @author Jorge
 */
public class Cliente {
    private Integer usu_id;
    private String usu_nombre;
    private String usu_apellidos;
    private String usu_nif;
    private String usu_dire;
    private String usu_mail;
    private String usu_pass;

    public Cliente() {
    }

    public Integer getUsu_id() {
        return usu_id;
    }

    public void setUsu_id(Integer usu_id) {
        this.usu_id = usu_id;
    }

    public String getUsu_nombre() {
        return usu_nombre;
    }

    public void setUsu_nombre(String usu_nombre) {
        this.usu_nombre = usu_nombre;
    }

    public String getUsu_apellidos() {
        return usu_apellidos;
    }

    public void setUsu_apellidos(String usu_apellidos) {
        this.usu_apellidos = usu_apellidos;
    }

    public String getUsu_nif() {
        return usu_nif;
    }

    public void setUsu_nif(String usu_nif) {
        this.usu_nif = usu_nif;
    }

    public String getUsu_dire() {
        return usu_dire;
    }

    public void setUsu_dire(String usu_dire) {
        this.usu_dire = usu_dire;
    }

    public String getUsu_mail() {
        return usu_mail;
    }

    public void setUsu_mail(String usu_mail) {
        this.usu_mail = usu_mail;
    }

    public String getUsu_pass() {
        return usu_pass;
    }

    public void setUsu_pass(String usu_pass) {
        this.usu_pass = usu_pass;
    }

    
    
    
}
